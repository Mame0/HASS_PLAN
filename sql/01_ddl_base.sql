CREATE INDEX idx_des_semana          ON despacho_semanal (semana_id);
CREATE INDEX idx_alerta_campana      ON alerta (campana_id);
CREATE INDEX idx_alerta_semana       ON alerta (semana_id);
CREATE INDEX idx_cs_lote             ON clima_sync (lote_id);
